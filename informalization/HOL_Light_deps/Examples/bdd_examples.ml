let gra001_1 =
`~((~v1 \/ v0) /\
  (~v0 \/ v1) /\
  (~v4 \/ ~v2 \/ ~v0) /\
  (~v4 \/ v2 \/ v0) /\
  (~v2 \/ v4 \/ v0) /\
  (~v0 \/ v4 \/ v2) /\
  (~v3 \/ ~v2 \/ ~v1) /\
  (~v3 \/ v2 \/ v1) /\
  (~v2 \/ v3 \/ v1) /\
  (~v1 \/ v3 \/ v2) /\
  (~v3 \/ ~v4) /\
  (v3 \/ v4))` in
BDD_TAUT gra001_1;;

