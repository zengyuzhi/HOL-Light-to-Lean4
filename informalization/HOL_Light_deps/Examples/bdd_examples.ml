let rip02_be =
 `(car1 <=> (a1 /\ b1)) /\
  (cout <=> ((a2 \/ b2) /\ car1 \/ a2 /\ b2)) /\
  (som2 <=> ~(a2 <=> ~(b2 <=> car1))) /\
  (som1 <=> ~(a1 <=> b1)) /\
  (cout1 <=> (b1 /\ a1))
  ==> (som1 <=> ~(~a1 /\ ~b1 \/ a1 /\ b1)) /\
      (som2 <=>
       ~((~a2 /\ ~b2 \/ a2 /\ b2) /\ ~cout1 \/
         cout1 /\ ~(~a2 /\ ~b2 \/ a2 /\ b2))) /\
      (cout <=> (a2 /\ cout1 \/ b2 /\ cout1 \/ a2 /\ b2))`;;

BDD_TAUT rip02_be;;
BDD_DEFTAUT [] rip02_be;;

